def plus(new_team, old_team):
    # Доработайте эту функцию, чтобы она возвращала сумму полученных параметров
    return new_team + old_team

def minus(new_team, old_team):
    # Доработайте эту функцию, чтобы она возвращала разность полученных параметров
    return new_team - old_team


sum = plus(8900, 4200)
different = minus(8900, 4200)
print(sum, different)