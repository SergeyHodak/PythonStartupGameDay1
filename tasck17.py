# min(список) - выдаст одно минимальне значение из списка
# max(список) - выдаст одно максимальное значение из списка
developer_rates = [600, 800, 1800, 2500, 3300]
# сохраните найменьшее число из списка в переменную min_rate
# созраните найбольшее число из списка в переменную max_rate
min_rate = min(developer_rates)
max_rate = max(developer_rates)
print(str(min_rate) + " " + str(max_rate))